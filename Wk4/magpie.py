input = int(input("Please enter a number: "))

if(input == 1):
    print("One for sorrow")
elif(input == 2):
    print("Two for joy")
elif(input == 3):
    print("Three for a girl")
elif(input == 4):
    print("Four for a boy")
elif(input == 5):
    print("Five for silver")
elif(input == 6):
    print("Six for gold")
elif(input == 7):
    print("Seven for a secret that should never be told")
else:
    print("Not a permitted number")